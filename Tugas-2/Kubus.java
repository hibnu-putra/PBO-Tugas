public class Kubus {

    public static void main(String[] args) {
        int sisi = 3;

        int luasPermukaan = 6 * sisi * sisi;
        int volume = sisi * sisi * sisi;

        System.out.println("Luas Permukaan Kubus: " + luasPermukaan);
        System.out.println("Volume Kubus: " + volume);
    }
}